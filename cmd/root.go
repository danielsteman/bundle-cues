// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "bundlecues",
	Short: "Validate Databricks asset bundles against custom rules",
	Long: `bundlecues is a CLI tool for validating and linting Databricks asset bundles.

You can define rules (cues) to enforce standards and best practices across your asset bundles.
This tool helps data platform teams ensure consistency, compliance, and maintainability of
Databricks workspace configurations.
`,
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

